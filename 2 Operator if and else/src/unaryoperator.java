public class unaryoperator {
    public static void main(String[] args) {
        int x= 5;
        int y = -x;
        System.out.println(y);
        int z =  -y;
        System.out.println(z);
        x =x+1;
        System.out.println(x);
        x+=1;
        System.out.println(x);
        x++;
        System.out.println(x);
        ++x;
        System.out.println(x);
        int p =5;
        System.out.println(p++);
        System.out.println(p);
        System.out.println(++p);
        System.out.println(p);
        System.out.println(p--);
        System.out.println(p);
        System.out.println(--p);
        System.out.println(p);

    }
}
